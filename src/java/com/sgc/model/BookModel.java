package com.sgc.model;

/**
 *
 * @author fasla faseel
 */
public class BookModel {
    String BookID;
    String Title;
    String Author;
    String mc;
    String sc;
    String yop;
    String lpy;
    String isbnNo;
    String noPg;
    public String getBookID() {
        return BookID;
    }

    public void setBookID(String BookID) {
        this.BookID = BookID;
    }

    public String getTitle() {
        return Title;
    }

    public void setTitle(String Title) {
        this.Title = Title;
    }

    public String getAuthor() {
        return Author;
    }

    public void setAuthor(String Author) {
        this.Author = Author;
    }

    public String getMc() {
        return mc;
    }

    public void setMc(String mc) {
        this.mc = mc;
    }

    public String getSc() {
        return sc;
    }

    public void setSc(String sc) {
        this.sc = sc;
    }

    public String getYop() {
        return yop;
    }

    public void setYop(String yop) {
        this.yop = yop;
    }

    public String getLpy() {
        return lpy;
    }

    public void setLpy(String lpy) {
        this.lpy = lpy;
    }

    public String getIsbnNo() {
        return isbnNo;
    }

    public void setIsbnNo(String isbnNo) {
        this.isbnNo = isbnNo;
    }

    public String getNoPg() {
        return noPg;
    }

    public void setNoPg(String noPg) {
        this.noPg = noPg;
    }
}
